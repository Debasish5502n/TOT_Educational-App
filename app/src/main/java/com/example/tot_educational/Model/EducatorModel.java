package com.example.tot_educational.Model;

public class EducatorModel {

    String ed_name, ed_image, ed_bio, ed_id;
    Long ed_follower;

    public String getEd_name() {
        return ed_name;
    }

    public void setEd_name(String ed_name) {
        this.ed_name = ed_name;
    }

    public String getEd_image() {
        return ed_image;
    }

    public void setEd_image(String ed_image) {
        this.ed_image = ed_image;
    }

    public String getEd_bio() {
        return ed_bio;
    }

    public void setEd_bio(String ed_bio) {
        this.ed_bio = ed_bio;
    }

    public String getEd_id() {
        return ed_id;
    }

    public void setEd_id(String ed_id) {
        this.ed_id = ed_id;
    }

    public Long getEd_follower() {
        return ed_follower;
    }

    public void setEd_follower(Long ed_follower) {
        this.ed_follower = ed_follower;
    }
}
