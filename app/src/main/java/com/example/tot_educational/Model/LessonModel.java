package com.example.tot_educational.Model;

public class LessonModel {

    String lessonTitle,lessonVideo,lessonImage,lessonId,courseTitle,lessonYoutubeVideo;

    public String getLessonTitle() {
        return lessonTitle;
    }

    public void setLessonTitle(String lessonTitle) {
        this.lessonTitle = lessonTitle;
    }

    public String getLessonVideo() {
        return lessonVideo;
    }

    public void setLessonVideo(String lessonVideo) {
        this.lessonVideo = lessonVideo;
    }

    public String getLessonImage() {
        return lessonImage;
    }

    public void setLessonImage(String lessonImage) {
        this.lessonImage = lessonImage;
    }

    public String getLessonId() {
        return lessonId;
    }

    public void setLessonId(String id) {
        this.lessonId = id;
    }

    public String getCourseTitle() {
        return courseTitle;
    }

    public void setCourseTitle(String courseTitle) {
        this.courseTitle = courseTitle;
    }

    public String getLessonYoutubeVideo() {
        return lessonYoutubeVideo;
    }

    public void setLessonYoutubeVideo(String lessonYoutubeVideo) {
        this.lessonYoutubeVideo = lessonYoutubeVideo;
    }
}
